package ex_09_Switch;

public class Lab095_Interview {
    public static void main(String[] args) {
        int a = 11;
        switch (-1){
            default:
                System.out.println("default");
                break;
            case -1:
                System.out.println("10");
                break;
            case 8:
                System.out.println("8");
                break;
        }
    }
}
