package ex_08_If_Condition;

public class Lab_IF_Else_If {
    public static void main(String[] args) {
        int value = 35;
        if (value > 35)
            System.out.println("this 1st print statement will execute");
        else if (value < 40 ) {
            System.out.println("This 2nd print statement will execute");
        }
        else
            System.out.println("This 3rd print statement will execute");
    }
}
