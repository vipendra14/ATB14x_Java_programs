package ex_10_For_Loop;

public class Lab109_Greater_vs_Equal {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        for (int i = 1; i <=10 ; i++) {
            System.out.println(i);
        }
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
    }
}
